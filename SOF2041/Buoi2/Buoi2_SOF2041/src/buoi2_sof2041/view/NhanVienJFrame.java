    private void detail(NhanVien nhanVien) {
        txtMa.setText(nhanVien.getMa());
        txtTen.setText(nhanVien.getTen());
        txtSDT.setText(nhanVien.getSdt());
        
        if (nhanVien.getTrangThai() == 0) {
            rdDangLam.setSelected(true);
        } else {
            rdNghiViec.setSelected(true);
        }
    }
    
    private void fillToTable(List<NhanVien> nhanVienLists) {
        int stt = 0;
        dtm.setRowCount(0);
        
        for (NhanVien nhanVien : nhanVienLists) {
            Object data[] = {
                stt+=1,
                nhanVien.getMa(),
                nhanVien.getTen(),
                nhanVien.getSdt(),
                nhanVien.getTrangThai() == 0 ? "Đang làm" : "Nghỉ việc"
            };
            dtm.addRow(data);
        }
    }

