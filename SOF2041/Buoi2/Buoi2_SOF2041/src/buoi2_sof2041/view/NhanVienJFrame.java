    private void tblNhanVienMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMousePressed
        // TODO add your handling code here:
        int selectedRow = tblNhanVien.getSelectedRow();
        String maNV = (String) tblNhanVien.getValueAt(selectedRow, 1);
        NhanVien nhanVien = nhanVienRepository.detail(maNV);
        detail(nhanVien);
    }//GEN-LAST:event_tblNhanVienMousePressed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String maNV = txtMa.getText();
        String ten = txtTen.getText();
        String sdt = txtSDT.getText();
        int trangThai = 0;
        
        if (rdNghiViec.isSelected()) {
            trangThai = 1;
        } else {
            trangThai = 0;
        }
        
        NhanVien nhanVien = new NhanVien(maNV, ten, sdt, trangThai);
        try {
            JOptionPane.showMessageDialog(this, nhanVienRepository.update(nhanVien));
            fillToTable(nhanVienRepository.getAll());
        } catch (SQLException ex) {
            Logger.getLogger(NhanVienJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

