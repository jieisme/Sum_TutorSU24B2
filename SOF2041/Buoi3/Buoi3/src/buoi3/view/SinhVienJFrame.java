    private void fillToTable(List<SinhVien> sinhVienLists) {
        dtm.setRowCount(0);
        int stt = 0;
        
        for (SinhVien sinhVien : sinhVienLists) {
            Object data[] = {
                stt++,
                sinhVien.getMaSV(),
                sinhVien.getTenSV(),
                sinhVien.isGioiTinh() == true ? "Nam" : "Nữ",
                sinhVien.getTuoi()
            };
            dtm.addRow(data);
        }
    }

