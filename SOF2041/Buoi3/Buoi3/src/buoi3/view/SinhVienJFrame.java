    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        int namSinh = Integer.parseInt(txtNamSinh.getText());
        boolean gioiTinh = false;
        if (rdNam.isSelected()) {
            gioiTinh = true;
        } else {
            gioiTinh = false;
        }
        
        SinhVien sinhVien = new SinhVien(ma, ten, gioiTinh, namSinh);
        try {
            JOptionPane.showMessageDialog(this, sinhVienRepository.add(sinhVien));
            fillToTable(sinhVienRepository.getAll());
        } catch (SQLException ex) {
            Logger.getLogger(SinhVienJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        DefaultTableModel searchTable = (DefaultTableModel) tblSinhVien.getModel();
        searchTable.setRowCount(0);
        try {
            fillToTable(sinhVienRepository.searchByMa(ma));
        } catch (SQLException ex) {
            Logger.getLogger(SinhVienJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

